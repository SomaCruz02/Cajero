        //SE REGISTRA LOS DATOS DE LA SESION INICIADA
        File s = new File(ubi2+"record.txt");
        if(s.exists()){
            
            try{
                FileInputStream fis1 = new FileInputStream(s);
                Properties mostrar1 = new Properties();
                mostrar1.load(fis1);               

                P = new ulog(mostrar1.getProperty("Nombre"),
                                    mostrar1.getProperty("Tarjeta"),
                                    mostrar1.getProperty("PIN"),
                                    Integer.parseInt(mostrar1.getProperty("Limite")),
                                    mostrar1.getProperty("Fecha"),
                                    mostrar1.getProperty("Hora"));
                b.add(P);

            }catch(Exception e){}
            
        }
        
        for(int i=0;i<registros.length;i++){
        
            File url = new File(ubi1+registros[i].getName());
            
            try{
                FileInputStream fis = new FileInputStream(url);
                Properties mostrar = new Properties();
                mostrar.load(fis);               
                if(mostrar.getProperty("Tarjeta").equals(b.get(0).Tarjeta)){
                    O = new trns(mostrar.getProperty("Tarjeta"),
                                        mostrar.getProperty("Fecha"),
                                        mostrar.getProperty("Hora"),
                                        Integer.parseInt(mostrar.getProperty("Monto")),
                                        mostrar.getProperty("Tipo"));
                    a.add(O);
                }
            }catch(Exception e){}
            
        }
        
        //Estilos de Tabla
        JTableHeader tb = TablaTrans.getTableHeader();
        tb.setOpaque(false);
        tb.setFont(new Font("Tahoma",Font.BOLD,18));
        tb.setForeground(new Color(236,240,241));
        TablaTrans.setRowHeight(30);
