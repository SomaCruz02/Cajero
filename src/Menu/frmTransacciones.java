    public void Tabla(){
        int h = 0;
        DefaultTableModel TblSim = (DefaultTableModel) TablaTrans.getModel();
        
        TblSim.setRowCount(0);
        for(int i=(a.size()-1); i>=0;i--){
            
            if(a.get(i).Tarjeta.equals(b.get(0).Tarjeta)){
                if(h==5){break;}
                Object Rfila1[] = {a.get(i).Tipo,"Q "+a.get(i).Monto,a.get(i).Fecha+" -- Hora: "+a.get(i).Hora};
                if(Tipo.getSelectedIndex() == 1){                  

                    if(a.get(i).Tipo.equals("Retiro")){
                        if(Historial.getSelectedIndex() == 1){
                            h++;
                        }
                        
                        TblSim.addRow(Rfila1);
                    }

                }else if(Tipo.getSelectedIndex() == 2){

                    if(a.get(i).Tipo.equals("Deposito")){
                        if(Historial.getSelectedIndex() == 1){
                            h++;
                        }
                        
                        TblSim.addRow(Rfila1);
                    }
                }else{
                    if(Historial.getSelectedIndex() == 1){
                        h++;
                        
                    }
                    
                    TblSim.addRow(Rfila1);
                }
                
            }
            
        }
        
        Ajustar();
    }
    
