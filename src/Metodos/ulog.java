package Metodos;


public class ulog {
    public String Nombre;
    public String Tarjeta;
    public String Pin;
    public int limite;
    public String Fecha;
    public String Hora;
    
    public ulog(String Nombre, String Tarjeta, String Pin, int limite, String Fecha, String Hora){
        this.Nombre = Nombre;
        this.Tarjeta = Tarjeta;
        this.Pin = Pin;
        this.limite = limite;
        this.Fecha = Fecha;
        this.Hora = Hora;
    }
}

