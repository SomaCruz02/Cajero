package Metodos;


public class trns {
    public String Tarjeta;
    public String Fecha;
    public String Hora;
    public int Monto;
    public String Tipo;
    
    public trns(String Tarjeta, String Fecha, String Hora, int Monto, String Tipo){
        this.Tarjeta = Tarjeta;
        this.Fecha = Fecha;
        this.Hora = Hora;
        this.Monto = Monto;
        this.Tipo = Tipo;
    }
}
