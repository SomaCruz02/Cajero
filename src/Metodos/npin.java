
package Metodos;


public class npin {
    public String Tarjeta;
    public String Fecha;
    public String PIN;
    public String NuevoPIN;
    
    public npin(String Tarjeta, String Fecha, String PIN, String NuevoPIN){
        this.Tarjeta = Tarjeta;
        this.Fecha = Fecha;
        this.PIN = PIN;
        this.NuevoPIN = NuevoPIN;
    }
}
