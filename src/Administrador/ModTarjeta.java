public void Modificar(){

        for(int i = 0; i<a.size();i++){
             if(usuarios.getSelectedItem().toString().equals(a.get(i).Tarjeta.toUpperCase())){
                 a.get(i).Tarjeta = notarjeta.getText().toUpperCase();
                 Crear(i);
                 break;
             }             
        }
        
        
        
    }

