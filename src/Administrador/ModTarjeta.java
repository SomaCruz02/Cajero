    private void btncrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncrearActionPerformed
        // TODO add your handling code here:

       Boolean E = false;
        
        if(notarjeta.getText().length()<16 || notarjeta.getText().length()>16){
            JOptionPane.showMessageDialog(null, "Verificar el Numero de la Tarjeta", "Error", JOptionPane.ERROR_MESSAGE);
         
            E = true;
        }
        
        for(int i = 0; i<a.size();i++){
             if(a.get(i).Tarjeta.equals(notarjeta.getText().toUpperCase())){
                 JOptionPane.showMessageDialog(null, "El Numero de la Tarjeta ya Existe", "Error", JOptionPane.ERROR_MESSAGE);
                 E = true;
               
                 break;
             }
        }
        
        if(!E){
            Modificar();
            JOptionPane.showMessageDialog(null, "El numero de Tarjeta fue Cambiado con Exito", "Usuario", JOptionPane.INFORMATION_MESSAGE);
        }else{
            
        }

    }//GEN-LAST:event_btncrearActionPerformed

