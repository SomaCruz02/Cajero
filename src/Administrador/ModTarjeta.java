private void Crear(int i){
        String archivo = ("user")+(i+1)+".txt";
        File crea_ubicación = new File(ruta);
                
        try{    
            crea_ubicación.mkdirs();
            Formatter crear = new Formatter(ruta+archivo);
            crear.format("%s\r\n%s\r\n%s\r\n%s\r\n%s\r\n%s",
                    "Nombre="+a.get(i).Nombre,
                    "Tarjeta="+notarjeta.getText().toUpperCase(),
                    "PIN="+a.get(i).Pin,
                    "Limite="+a.get(i).limite,
                    "Saldo="+a.get(i).saldo,
                    "Tipo="+a.get(i).tipo);
            crear.close();          
            
        }catch(Exception e){}    
        
        llenar();
    }

